# operadores

# operador de asignación: =

# operadors matemáticos: + - * /
print(12+10)
print(17//5) # // (división entera)
print(17%5) #  % (módulo/resto)
print(15%5)
print(2**3) # ** (potencia)
print(4**(1/2)) # Raíz cuadrada de 2

# operadores relaciones: = > < == != >= <=
